-- ----------------------------
-- Table structure for `ACCOUNTS`
-- ----------------------------
CREATE TABLE IF NOT EXISTS `ACCOUNTS` (
  `ID` INT NOT NULL,
  `NAME` VARCHAR(255) NOT NULL,
  `PASSWORD` VARCHAR(255) NOT NULL,
  `ACCESS_LEVEL` INT NOT NULL DEFAULT '0',
  `CHARACTER_SLOT` TINYINT NOT NULL DEFAULT '0',
  `LAST_ACTIVATED_AT` DATETIME DEFAULT NULL,
  `IP` VARCHAR(255) DEFAULT NULL,
  `HOST` VARCHAR(255) DEFAULT NULL,
  `IS_ACTIVE` BOOLEAN NOT NULL DEFAULT '1'
);
-- ----------------------------
-- Index for `ACCOUNTS`
-- ----------------------------
CREATE PRIMARY KEY IF NOT EXISTS `ACCOUNTS_PK` ON `ACCOUNTS`(`ID`);
CREATE UNIQUE INDEX IF NOT EXISTS `ACCOUNTS_UK` ON `ACCOUNTS`(`NAME`);
